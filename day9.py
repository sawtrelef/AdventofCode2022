class Tail():
    def __init__(self,pos,head):
        self.head = head
        self.position = [int(pos[0]), int(pos[1])]
        self.PosDict = {str(self.position): 1}
        self.next = False

    def setnext(self, thing):
        self.next = thing

    def Follow(self):
        headcoords = self.head.position
        mycoords = self.position
        dif = [headcoords[0] - mycoords[0], headcoords[1] - mycoords[1]]
        if abs(int(dif[0])) >= 2 or abs(int(dif[1])) >= 2:
            movecoords = [0,0]
            if int(dif[0]) < 0:
                movecoords[0] = movecoords[0] - 1
            elif int(dif[0]) > 0:
                movecoords[0] =  movecoords[0] + 1
            if int(dif[1]) < 0:
                movecoords[1] = movecoords[1] - 1
            elif int(dif[1]) > 0:
                movecoords[1] =  movecoords[1] + 1

            self.position = [self.position[0] + movecoords[0], self.position[1] + movecoords[1]]
            self.report()
            if self.next:
                self.next.Follow()

    def report(self):
        if str(self.position) in self.PosDict:
            count = self.PosDict[str(self.position)] + 1
            self.PosDict[str(self.position)] = count
        else:
            self.PosDict[str(self.position)] = 1

    def GetVisitedNumber(self):
        length = len(self.PosDict)

        return length


